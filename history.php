		        <h2>History of Mt. Washington and Pairpoint Glass</h2>
		        <p>The seaport city of New Bedford, Massachusetts, once famous as a whaling center and featured as the home port of Herman Melville's novel Moby Dick, also hosted a thriving glass industry in the late 19th and early 20th centuries. Foremost among its glass factories was the Mt. Washington Glass Works, originally founded in 1837 in South Boston (in the vicinity of a small hill known as &quot;Mt. Washington&quot;). It relocated to New Bedford in 1870 where it produced a wide range of pressed glass, fine cut and engraved glass, lighting and other products. At the 1876 Centennial Exhibition in Philadelphia the company displayed a 17 foot high crystal fountain illuminated with 120 gas jets that 'presented a spectacle of fairy beauty almost beyond imagination,' to quote a visitor to the fair.</p>
		        <p>From the late 1870s to the 1890s, Mt. Washington introduced a series of brilliant, innovative and exotic glass formulae and decorative treatments that established New Bedford as the Art Glass Headquarters of the country. &quot;Bronze glass&quot; suggested the iridescent shimmer of excavated ancient glass. Amberina shaded imperceptibly from ruby red to rich amber. Burmese glass, with its delicate salmon pink and translucent yellow colors, suggested the splendor of a Burma sunset. Examples of Burmese glass were sent in 1886 to both President Grover Cleveland and Queen Victoria and created an instant sensation. Other outstanding art glass lines by the company included Peachblow, Crown Milano, Royal Flemish, Cameo, Pearl Satin and Coraline glass, to name just a few.</p>
		        <p>In 1880, the Pairpoint Manufacturing Company (named after its first superintendent, Thomas J. Pairpoint, who was considered one of the greatest silver designers in England and America) was established in New Bedford to supply Mt. Washington with attractive silver-plated metal mounts. The companies thrived and in 1894 they merged and became known as the Pairpoint Corporation. Large quantities of translucent white glass were produced for colorful enamel decoration, and in the early 20th century the company's distinctive decorated lampshades gained international popularity. Pairpoint continued to prosper through the 1920s and early 1930s, when bold color and a Scandinavian design ethos prevailed. By 1939, however, foreign competition resulted in the closing of the factory. Successor firms, including the Gundersen Glass Works and Gundersen-Pairpoint, continued production in New Bedford until 1957. Today an offshoot of the original company, Pairpoint Glass Works, Inc., operates in Sagamore, MA, on nearby Cape Cod. </p>
