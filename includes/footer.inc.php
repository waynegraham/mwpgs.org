		<h3>History of Mt. Washington and Pairpoint Glass</h3>
		<p>
			The seaport city of New Bedford, Massachusetts, once famous as a whaling center and featured
			as the home port of Herman Melville's novel <em>Moby Dick</em>, also hosted a thriving 
			glass industry in the late 19<sup>th</sup> and early 20<sup>th</sup> centuries. Foremost
			among its glass factories was the Mt. Washington Glass Works, originally founded in 1837 in South 
			Boston (in the vicinity of a small hill known as "Mt. Washington"). It relocated to New
			Bedford in 1870 where it  produced a wide range of pressed glass, find cut and
			engraved glass, lighting and other products. At the 1876 Centennial Exhibition in
			Philadelphia the company displayed a 17 foot high crystal fountain illuminated with 120
			gas jets that 'presented a spectacle of fairy beauty almost behond imagination,' to quote
			a visitor to the fair.
		</p>
		
